// ═══ TEMPLATE 4: IMPACT — Split screen with benefiting/harmed ═══
function generateImpactScene(scene, locale, color) {
  const isRTL = locale === 'ar';
  const title = escapeHTML(stripMarkdown(scene.title));
  const items = parseDisplayItems(scene.displayText);

  // Split items into benefiting / harmed
  // Universal parser — works for ALL languages (ar/en/fr/tr/es)
  const benefiting = [];
  const harmed = [];
  let section = null;
  for (const item of items) {
    const colonMatch = item.match(/^(.+?):\s*(.+)$/);
    if (colonMatch) {
      const prefix = colonMatch[1].toLowerCase();
      const content = colonMatch[2].trim();
      if (/يستفيد|مستفيد|benefit|gain|up|bullish|yarar|kazan|beneficio|ganancia/i.test(prefix)) {
        section = 'up';
        if (content) benefiting.push(content);
        continue;
      }
      if (/يتضرر|متضرر|harm|loss|down|bearish|zarar|kayb|perjuicio|p[eé]rdida/i.test(prefix)) {
        section = 'down';
        if (content) harmed.push(content);
        continue;
      }
    }
    if (section === 'up') benefiting.push(item);
    else if (section === 'down') harmed.push(item);
    else benefiting.push(item);
  }

  const benefitHTML = benefiting.map((item, i) => `
    <div style="display:flex;align-items:center;gap:10px;padding:14px 18px;border-radius:10px;background:${COLORS.bgCard};border:1px solid ${COLORS.green}30;margin-bottom:12px;opacity:0;animation:fadeUp .4s ease ${0.5+i*0.4}s forwards">
      <span style="font-size:20px;color:${COLORS.green};font-weight:700">▲</span>
      <span style="font-size:20px;color:${COLORS.textWhite}">${escapeHTML(item)}</span>
    </div>`).join('');

  const harmedHTML = harmed.map((item, i) => `
    <div style="display:flex;align-items:center;gap:10px;padding:14px 18px;border-radius:10px;background:${COLORS.bgCard};border:1px solid ${COLORS.red}30;margin-bottom:12px;opacity:0;animation:fadeUp .4s ease ${0.8+i*0.4}s forwards">
      <span style="font-size:20px;color:${COLORS.red};font-weight:700">▼</span>
      <span style="font-size:20px;color:${COLORS.textWhite}">${escapeHTML(item)}</span>
    </div>`).join('');

  return `<div style="width:100%;height:100%;display:flex;flex-direction:column;padding:60px 64px;justify-content:center">
    <div style="display:flex;align-items:center;gap:16px;margin-bottom:24px;opacity:0;animation:fadeUp .4s ease .1s both">
      <div style="width:12px;height:12px;border-radius:2px;background:${COLORS.gold};transform:rotate(45deg)"></div>
      <div style="font-size:14px;color:${COLORS.gold};font-weight:600;letter-spacing:2px;text-transform:uppercase">${isRTL ? 'التأثير' : 'IMPACT'}</div>
    </div>
    <h2 style="font-size:44px;font-weight:600;color:${COLORS.textWhite};margin-bottom:32px;opacity:0;animation:fadeUp .4s ease .2s both">${title}</h2>
    <div style="display:flex;gap:32px">
      <div style="flex:1">
        <div style="font-size:18px;color:${COLORS.green};font-weight:600;margin-bottom:16px;display:flex;align-items:center;gap:8px">
          <div style="width:8px;height:8px;border-radius:50%;background:${COLORS.green}"></div>
          ${i18nLabel(locale,'benefiting')}
        </div>
        ${benefitHTML || `<div style="color:${COLORS.textDim}">—</div>`}
      </div>
      <div style="flex:1">
        <div style="font-size:18px;color:${COLORS.red};font-weight:600;margin-bottom:16px;display:flex;align-items:center;gap:8px">
          <div style="width:8px;height:8px;border-radius:50%;background:${COLORS.red}"></div>
          ${i18nLabel(locale,'harmed')}
        </div>
        ${harmedHTML || `<div style="color:${COLORS.textDim}">—</div>`}
      </div>
    </div>
  </div>`;
}

