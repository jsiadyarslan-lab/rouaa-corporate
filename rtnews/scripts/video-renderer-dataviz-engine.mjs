// ═══ Scene type → template ═══
const SCENE_TEMPLATES = {
  hook:       { color: COLORS.gold,  variants: [generateHookScene] },
  context:    { color: COLORS.gold,  variants: [generateContextScene] },
  data:       { color: COLORS.gold,  variants: [generateDataScene] },
  impact:     { color: COLORS.gold,  variants: [generateImpactScene] },
  resolution: { color: COLORS.gold,  variants: [generateResolutionScene] },
};

function selectTemplate(sceneType, sceneId) {
  const config = SCENE_TEMPLATES[sceneType] || SCENE_TEMPLATES.context;
  const variants = config.variants;
  const variantIndex = (sceneId - 1) % variants.length;
  return { color: config.color, template: variants[variantIndex] };
}

