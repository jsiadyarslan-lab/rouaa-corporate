// ═══ Main Pipeline ═══
async function main() {
  const startTime = Date.now();
  console.log('[Gold-V2] Starting... format=' + FORMAT + ' ' + WIDTH + 'x' + HEIGHT);
  const args = process.argv.slice(2);
  let inputPath = '', outputPath = '';
  for (let i = 0; i < args.length; i++) { if (args[i] === '--input' && args[i+1]) inputPath = args[++i]; if (args[i] === '--output' && args[i+1]) outputPath = args[++i]; }
  if (!inputPath || !outputPath) { console.error('Usage: node video-renderer-gold-engine.mjs --input scenes.json --output video.mp4'); process.exit(1); }
  const data = JSON.parse(readFileSync(inputPath, 'utf-8'));
  const locale = data.locale || 'ar';
  const scenes = data.scenes || [];
  const hasOutro = !!data.outroText;
  const totalScenes = scenes.length;
  const totalRenderScenes = hasOutro ? totalScenes + 1 : totalScenes;
  console.log(`[Gold-V2] Title: "${data.title?.slice(0, 50)}..." | Locale: ${locale} | Scenes: ${totalScenes}`);
  const tmpDir = join(tmpdir(), `gold-v2-${randomUUID()}`);
  mkdirSync(tmpDir, { recursive: true });

  // Step 1: Images
  const preGenerated = scenes.filter(s => s.imageBase64).length;
  if (preGenerated === scenes.length) { console.log(`[Gold-V2] Step 1: All ${preGenerated} images pre-loaded.`); }
  else {
    console.log(`[Renderer] WARNING: images missing — gradient fallback`);
    for (let i = 0; i < scenes.length; i++) {
      if (!scenes[i].imageBase64) scenes[i].imageBase64 = null;
    }
  }

  // Step 2: TTS
  console.log('[Gold-V2] Step 2: TTS...');
  const audioPaths = [], audioDurations = [];
  for (let i = 0; i < totalScenes; i++) {
    const scene = scenes[i];
    const narrationText = `${scene.title}. ${scene.narrationText}`;
    const audioPath = join(tmpDir, `scene_${i}.mp3`);
    const success = generateTTS(narrationText, audioPath, locale);
    if (success) { audioDurations.push(getAudioDuration(audioPath)); audioPaths.push(audioPath); console.log(`  [${i+1}/${totalScenes}] ✓ ${audioDurations[i].toFixed(1)}s`); }
    else { audioDurations.push(scene.duration || 6); audioPaths.push(null); console.log(`  [${i+1}/${totalScenes}] ✗`); }
  }
  if (hasOutro) { const p = join(tmpDir, `outro.mp3`); const s = generateTTS(data.outroText, p, locale); if (s) { audioDurations.push(getAudioDuration(p)); audioPaths.push(p); } else { audioDurations.push(5); audioPaths.push(null); } }

  // Step 3: Audio concat
  console.log('[Gold-V2] Step 3: Audio concat...');
  const fullAudioPath = join(tmpDir, 'full.mp3');
  const validAudio = audioPaths.filter(p => p);
  if (validAudio.length > 0) {
    const silencePath = join(tmpDir, 'silence.wav');
    spawnSync('ffmpeg', ['-y','-f','lavfi','-i','anullsrc=r=44100:cl=mono','-t','1.2',silencePath], { encoding: 'utf-8', timeout: 10000 });
    const listFile = join(tmpDir, 'list.txt');
    const listEntries = [];
    for (let i = 0; i < validAudio.length; i++) { listEntries.push(`file '${validAudio[i]}'`); if (i < validAudio.length - 1 && existsSync(silencePath)) listEntries.push(`file '${silencePath}'`); }
    writeFileSync(listFile, listEntries.join('\n'));
    spawnSync('ffmpeg', ['-y','-f','concat','-safe','0','-i',listFile,'-c:a','libmp3lame','-b:a','192k',fullAudioPath], { encoding: 'utf-8', timeout: 60000 });
    const procPath = fullAudioPath.replace('.mp3', '_proc.mp3');
    spawnSync('ffmpeg', ['-y','-i',fullAudioPath,'-af','loudnorm=I=-16:LRA=11:TP=-1.5','-c:a','libmp3lame','-b:a','192k',procPath], { encoding: 'utf-8', timeout: 120000 });
    if (existsSync(procPath)) { try { unlinkSync(fullAudioPath); } catch {} renameSync(procPath, fullAudioPath); }
  }

  // Step 4: Render frames
  console.log('[Gold-V2] Step 4: Render frames...');
  const framesDir = join(tmpDir, 'frames');
  mkdirSync(framesDir, { recursive: true });
  const browser = await chromium.launch({ headless: true });
  const page = await browser.newPage({ viewport: { width: WIDTH, height: HEIGHT } });
  let frameCount = 0;
  const FADE_OUT_FRAMES = 12, FADE_IN_FRAMES = 12;
  for (let s = 0; s < totalRenderScenes; s++) {
    const isOutro = s === totalScenes;
    const scene = isOutro ? {} : scenes[s];
    const dur = isOutro ? Math.max(5, audioDurations[s] || 5) : Math.max(scene.duration || 6, audioDurations[s] || 6);
    const totalFrames = Math.ceil(dur * FPS);
    const sceneStartMs = Date.now();
    console.log(`  [Scene ${s+1}/${totalRenderScenes}] ${isOutro ? 'OUTRO' : scene.title} — ${dur.toFixed(1)}s (${totalFrames} frames)`);
    const html = isOutro ? generateSceneHTML(null, -1, totalScenes, data) : generateSceneHTML(scene, s, totalScenes, data);
    try { await page.setContent(html, { waitUntil: 'domcontentloaded' }); } catch (e) { console.error(`  [Scene ${s+1}] setContent failed: ${e.message}`); continue; }
    await page.waitForTimeout(300);
    try { await page.evaluate(() => { const bg = document.getElementById('bg'); if (bg) { bg.style.backgroundSize = '100% 100%'; bg.style.backgroundPosition = '50% 50%'; } }); } catch {}
    const isFirstScene = s === 0;
    const fadeInFrames = isFirstScene ? 0 : FADE_IN_FRAMES;
    const motionDir = scene.motionDirection || 'zoom-in';
    for (let f = 0; f < fadeInFrames; f++) {
      const fadeInOp = (f + 1) / fadeInFrames;
      const sp = easeInOutCubic((f + 1) / totalFrames);
      const sz = motionDir === 'zoom-in' ? 100 + 25 * sp : motionDir === 'zoom-out' ? 125 - 25 * sp : motionDir === 'static' ? 100 + 5 * sp : 110 + 8 * sp;
      const px = motionDir === 'zoom-in' ? 50 - 6 * sp : motionDir === 'zoom-out' ? 44 + 6 * sp : motionDir === 'pan-right' ? 42 + 16 * sp : motionDir === 'pan-left' ? 58 - 16 * sp : 50;
      const py = motionDir === 'zoom-in' ? 50 - 4 * sp : motionDir === 'zoom-out' ? 46 + 4 * sp : motionDir === 'static' ? 50 - 1 * sp : 50;
      try { await page.evaluate(({ sz, px, py, op }) => { const bg = document.getElementById('bg'); if (bg) { bg.style.backgroundSize = sz + '%'; bg.style.backgroundPosition = px + '% ' + py + '%'; } document.body.style.opacity = String(op); }, { sz, px, py, op: fadeInOp }); } catch {}
      try { await page.screenshot({ path: join(framesDir, `frame_${String(frameCount).padStart(5, '0')}.jpg`), type: 'jpeg', quality: 90 }); } catch {}
      frameCount++;
    }
    try { await page.evaluate(() => { document.body.style.opacity = '1'; }); } catch {}
    const mainFrames = totalFrames - fadeInFrames;
    for (let f = 0; f < mainFrames; f++) {
      const lp = (f + fadeInFrames + 1) / totalFrames;
      const ep = easeInOutCubic(lp);
      let kbSize, kbPosX, kbPosY;
      if (motionDir === 'static' || isOutro) { kbSize = 100 + 5 * ep; kbPosX = 50; kbPosY = 50 - 1 * ep; }
      else if (motionDir === 'zoom-in') { kbSize = 100 + 25 * ep; kbPosX = 50 - 6 * ep; kbPosY = 50 - 4 * ep; }
      else if (motionDir === 'zoom-out') { kbSize = 125 - 25 * ep; kbPosX = 44 + 6 * ep; kbPosY = 46 + 4 * ep; }
      else if (motionDir === 'pan-right') { kbSize = 110 + 8 * ep; kbPosX = 42 + 16 * ep; kbPosY = 50; }
      else if (motionDir === 'pan-left') { kbSize = 110 + 8 * ep; kbPosX = 58 - 16 * ep; kbPosY = 50; }
      else { kbSize = 100 + 15 * ep; kbPosX = 50; kbPosY = 50; }
      const progressPct = ((f + fadeInFrames + 1) / totalFrames) * 100;
      try { await page.evaluate(({ sz, px, py, p }) => { const bg = document.getElementById('bg'); if (bg) { bg.style.backgroundSize = sz + '%'; bg.style.backgroundPosition = px + '% ' + py + '%'; } const pf = document.querySelector('.progress-fill'); if (pf) pf.style.width = p + '%'; }, { sz: kbSize, px: kbPosX, py: kbPosY, p: progressPct }); } catch {}
      try { await page.screenshot({ path: join(framesDir, `frame_${String(frameCount).padStart(5, '0')}.jpg`), type: 'jpeg', quality: 90 }); } catch {}
      frameCount++;
    }
    if (s < totalRenderScenes - 1) {
      for (let tf = 0; tf < FADE_OUT_FRAMES; tf++) {
        const op = (tf + 1) / FADE_OUT_FRAMES;
        try { await page.evaluate(o => { document.body.style.opacity = String(1 - o); }, op); } catch {}
        try { await page.screenshot({ path: join(framesDir, `frame_${String(frameCount).padStart(5, '0')}.jpg`), type: 'jpeg', quality: 90 }); } catch {}
        frameCount++;
      }
      try { await page.evaluate(() => { document.body.style.opacity = '1'; }); } catch {}
    }
    console.log(`  [Scene ${s+1}] DONE in ${((Date.now() - sceneStartMs) / 1000).toFixed(1)}s`);
  }
  await browser.close();
  console.log(`[Gold-V2] Total frames: ${frameCount}`);

  // Step 5: FFmpeg
  console.log('[Gold-V2] Step 5: FFmpeg encode...');
  const videoOnlyPath = join(tmpDir, 'video.mp4');
  spawnSync('ffmpeg', ['-y','-framerate',String(FPS),'-i',join(framesDir,'frame_%05d.jpg'),'-vf','format=yuv420p','-pix_fmt','yuv420p','-c:v','libx264','-preset','fast','-crf','18','-maxrate','8M','-movflags','+faststart',videoOnlyPath], { encoding: 'utf-8', timeout: 600000 });

  // Step 6: Merge
  console.log('[Gold-V2] Step 6: Merge...');
  if (existsSync(fullAudioPath)) { spawnSync('ffmpeg', ['-y','-i',videoOnlyPath,'-i',fullAudioPath,'-c:v','copy','-c:a','aac','-b:a','192k','-ac','2','-shortest','-movflags','+faststart',outputPath], { encoding: 'utf-8', timeout: 120000 }); }
  else { renameSync(videoOnlyPath, outputPath); }
  try { rmSync(tmpDir, { recursive: true, force: true }); } catch {}
  console.log(`[Gold-V2] ✅ Done — ${((Date.now() - startTime) / 1000).toFixed(1)}s | ${(statSync(outputPath).size / 1024 / 1024).toFixed(1)}MB`);
}

main().catch(err => { console.error('[Gold-V2] FATAL:', err.message); process.exit(1); });
