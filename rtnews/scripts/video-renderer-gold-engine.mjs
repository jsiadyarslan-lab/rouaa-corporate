  const items = parseDisplayItems(scene.displayText);
  const label = (SCENE_TYPE_LABELS[locale] || SCENE_TYPE_LABELS.en).impact;
  // Universal parser — works for ALL languages (ar/en/fr/tr/es)
  const benefiting = []; const harmed = []; let section = null;
  for (const item of items) {
    const colonMatch = item.match(/^(.+?):\s*(.+)$/);
    if (colonMatch) {
      const prefix = colonMatch[1].toLowerCase();
      const content = colonMatch[2].trim();
      if (/يستفيد|مستفيد|benefit|gain|up|bullish|yarar|kazan|beneficio|ganancia/i.test(prefix)) {
        section = 'up';
        if (content) benefiting.push(content);
        continue;
      }
      if (/يتضرر|متضرر|harm|loss|down|bearish|zarar|kayb|perjuicio|p[eé]rdida/i.test(prefix)) {
        section = 'down';
        if (content) harmed.push(content);
        continue;
      }
    }
    if (section === 'up') benefiting.push(item); else if (section === 'down') harmed.push(item); else benefiting.push(item);
  }
  const benHTML = benefiting.map((item,i) => `<div style="display:flex;align-items:center;gap:10px;padding:14px 20px;border-radius:10px;background:${COLORS.bgCard};border:1px solid ${COLORS.green}25;margin-bottom:10px;opacity:0;animation:fadeUp .4s ease ${0.5+i*0.3}s forwards"><span style="font-size:22px;color:${COLORS.green};font-weight:700">▲</span><span class="dv-item" style="font-size:22px;padding:0">${escapeHTML(item)}</span></div>`).join('');
  const harHTML = harmed.map((item,i) => `<div style="display:flex;align-items:center;gap:10px;padding:14px 20px;border-radius:10px;background:${COLORS.bgCard};border:1px solid ${COLORS.red}25;margin-bottom:10px;opacity:0;animation:fadeUp .4s ease ${0.8+i*0.3}s forwards"><span style="font-size:22px;color:${COLORS.red};font-weight:700">▼</span><span class="dv-item" style="font-size:22px;padding:0">${escapeHTML(item)}</span></div>`).join('');
