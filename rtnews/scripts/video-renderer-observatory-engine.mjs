function escapeHTML(str) { return String(str || '').replace(/[&<>"']/g, c => ({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[c])); }
function stripMarkdown(text) { if (!text) return ''; return String(text).replace(/\*\*(.+?)\*\*/g, '$1').replace(/\*(.+?)\*/g, '$1').replace(/`([^`]+)`/g, '$1').replace(/#{1,6}\s+/g, '').replace(/\[([^\]]+)\]\([^)]+\)/g, '$1').replace(/<[^>]+>/g, '').trim(); }
function toArabicNumeral(n) { return String(n).split('').map(d => '٠١٢٣٤٥٦٧٨٩'[parseInt(d)] || d).join(''); }
function parseDisplayItems(text) { if (!text) return []; return stripMarkdown(text).split(/\s*[|›]\s*/).map(s => s.trim()).filter(s => s.length > 0); }
function parseStatItem(text) { const m = text.match(/^(.+?):\s*([+\-]?[\d.]+)\s*(%?)/); if (m) return { label: m[1].trim(), value: m[2]+(m[3]||''), numericValue: parseFloat(m[2]), isNegative: m[2].startsWith('-'), raw: text }; return { label: text, value: '', numericValue: null, isNegative: false, raw: text }; }

function generateTTS(text, outputPath, locale, maxRetries = 3) {
  const voice = TTS_VOICES[locale] || TTS_VOICES.ar;
  const tmpFile = outputPath.replace('.mp3', '_text.txt');
  writeFileSync(tmpFile, text, 'utf-8');
  const py = `import asyncio, edge_tts\nasync def main():\n    text = open("${tmpFile}", "r", encoding="utf-8").read()\n    comm = edge_tts.Communicate(text, "${voice}", rate="-5%", pitch="+0Hz")\n    await comm.save("${outputPath}")\nasyncio.run(main())`;
  for (let attempt = 1; attempt <= maxRetries; attempt++) {
    const result = spawnSync('python3', ['-c', py], { encoding: 'utf-8', timeout: 60000 });
    if (result.status === 0 && existsSync(outputPath) && statSync(outputPath).size > 1000) { try { unlinkSync(tmpFile); } catch {} return true; }
    if (attempt < maxRetries) { try { if (existsSync(outputPath)) unlinkSync(outputPath); } catch {} spawnSync('sleep', ['2'], { timeout: 3000 }); }
  }
  try { unlinkSync(tmpFile); } catch {}
  return false;
}

function getAudioDuration(path) { try { const r = spawnSync('ffprobe', ['-v','quiet','-show_entries','format=duration','-of','default=noprint_wrappers=1:nokey=1', path], { encoding: 'utf-8', timeout: 10000 }); return parseFloat(r.stdout?.trim()) || 0; } catch { return 0; } }
function easeInOutCubic(t) { return t < 0.5 ? 4 * t * t * t : 1 - Math.pow(-2 * t + 2, 3) / 2; }

