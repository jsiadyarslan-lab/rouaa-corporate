// ═══ TEMPLATE: IMPACT — title + assets split (benefiting/harmed) ═══
function generateImpactScene(scene, locale, color) {
  const isRTL = locale === 'ar';
  const title = escapeHTML(stripMarkdown(scene.title));
  const items = parseDisplayItems(scene.displayText);

  // Universal parser — works for ALL languages (ar/en/fr/tr/es)
  const benefiting = []; const harmed = []; let section = null;
  for (const item of items) {
    const colonMatch = item.match(/^(.+?):\s*(.+)$/);
    if (colonMatch) {
      const prefix = colonMatch[1].toLowerCase();
      const content = colonMatch[2].trim();
      if (/يستفيد|مستفيد|benefit|gain|up|bullish|yarar|kazan|beneficio|ganancia/i.test(prefix)) {
        section = 'up';
        if (content) benefiting.push(content);
        continue;
      }
      if (/يتضرر|متضرر|harm|loss|down|bearish|zarar|kayb|perjuicio|p[eé]rdida/i.test(prefix)) {
        section = 'down';
        if (content) harmed.push(content);
        continue;
      }
    }
    if (section === 'up') benefiting.push(item); else if (section === 'down') harmed.push(item); else benefiting.push(item);
  }

  const benHTML = benefiting.map(a => `<div class="asset-row" style="border-color:${C.green}30"><div><div class="asset-name">${escapeHTML(a)}</div></div><div class="asset-sym" style="color:${C.green}">▲</div></div>`).join('');
  const harHTML = harmed.map(a => `<div class="asset-row" style="border-color:${C.red}30"><div><div class="asset-name">${escapeHTML(a)}</div></div><div class="asset-sym" style="color:${C.red}">▼</div></div>`).join('');

  return `<div class="content"><div class="content-box">
    <div class="scene-title">${title}</div>
    <div class="assets">
      <div class="asset-col"><div class="asset-title" style="color:${C.green}">▲ ${i18nLabel(locale,'benefiting')}</div>${benHTML||'<div class="asset-sym">—</div>'}</div>
      <div class="asset-col"><div class="asset-title" style="color:${C.red}">▼ ${i18nLabel(locale,'harmed')}</div>${harHTML||'<div class="asset-sym">—</div>'}</div>
    </div>
  </div></div>`;
}

