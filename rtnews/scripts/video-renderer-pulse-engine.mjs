// ═══ DATA PARSER — extracts real numbers/labels from displayText ═══
function parseDisplayItems(text) {
  if (!text) return [];
  return stripMarkdown(text).split(/\s*[|›]\s*/).map(s => s.trim()).filter(s => s.length > 0);
}

// Parse "label: value%" or "label: value" → {label, value, numericValue, unit}
function parseStatItem(text) {
  const match = text.match(/^(.+?):\s*([+\-]?[\d.]+)\s*(%?)/);
  if (match) {
    return {
      label: match[1].trim(),
      value: match[2] + (match[3] || ''),
      numericValue: parseFloat(match[2]),
      unit: match[3] || '',
      isNegative: match[2].startsWith('-'),
      raw: text,
    };
  }
  // No number — just label
  return { label: text, value: '', numericValue: null, unit: '', isNegative: false, raw: text };
}

// Parse "label: action at NUMBER" → {label, action, number}
function parseRecommendationItem(text) {
  const numMatch = text.match(/(\d[\d.,]*)/);
  const parts = text.split(':');
  return {
    label: parts[0]?.trim() || text,
    action: parts.slice(1).join(':').trim() || text,
    number: numMatch ? numMatch[1] : null,
    raw: text,
  };
}

