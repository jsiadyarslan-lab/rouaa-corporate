// V1042: Lesson body translation helpers (long-form content)
// ════════════════════════════════════════════════════════════════════
import { LESSON_BODIES_EN } from './academy-content-en';
import { LESSON_BODIES_FR } from './academy-content-fr';
import { LESSON_BODIES_TR } from './academy-content-tr';
import { LESSON_BODIES_ES } from './academy-content-es';

const LESSON_BODIES_BY_LOCALE: Record<Exclude<Locale, 'ar'>, Record<string, { content: string; keyPoints: string[]; practicalExample: string }>> = {
  en: LESSON_BODIES_EN,
  fr: LESSON_BODIES_FR,
  tr: LESSON_BODIES_TR,
  es: LESSON_BODIES_ES,
};

/** Returns translated lesson body (content, keyPoints, practicalExample) or Arabic fallback. */
export function getLessonBody(
  lessonId: string,
  locale: Locale,
  arabic: { content: string; keyPoints: string[]; practicalExample: string },
): { content: string; keyPoints: string[]; practicalExample: string } {
  if (locale === 'ar') return arabic;
  const table = LESSON_BODIES_BY_LOCALE[locale];
  if (table) {
    const translated = table[lessonId];
    if (translated) return translated;
  }
  return arabic;
}
