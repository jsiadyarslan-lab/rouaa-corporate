export interface AssetPrice {
  symbol: string;
  name: string;
  nameAr: string;
  price: number;
  change: number;
  changePercent: number;
  icon: string;
  sparklineData: number[];
}

export interface BreakingNewsItem {
  id: string;
  title: string;
  time: string;
  affectedAssets: { symbol: string; change: number }[];
  isBreaking: boolean;
  sentiment: 'positive' | 'negative' | 'neutral';
  sentimentScore: number;
  impactLevel: 'high' | 'medium' | 'low';
  originalLanguage: 'ar' | 'en';
}

export interface NewsItem {
  id: string;
  title: string;
  summary: string;
  time: string;
  category: string;
  sentiment: 'positive' | 'negative' | 'neutral';
  sentimentScore: number;
  impactLevel: 'high' | 'medium' | 'low';
  originalLanguage: 'ar' | 'en';
  imageUrl?: string;
}

export interface CentralBank {
  id: string;
  name: string;
  country: string;
  flag: string;
  currentRate: number;
  previousRate: number;
  nextMeetingDate: string;
  aiPrediction: 'hold' | 'raise' | 'cut';
  aiConfidence: number;
  currency: string;
  rateName: string;
}

export interface EconomicEvent {
  id: string;
  event: string;
  eventAr: string;
  time: string;
  impactLevel: 1 | 2 | 3;
  forecast: string;
  previous: string;
  country: string;
  affectedAssets: { symbol: string; direction: 'up' | 'down' | 'neutral' }[];
}

export interface ArabMarket {
  id: string;
  name: string;
  country: string;
  flag: string;
  value: number;
  change: number;
  openTime: string;
  closeTime: string;
  timezone: string;
  sparkline: number[];
  topStocks: { name: string; change: number }[];
}

export interface EarningsItem {
  symbol: string;
  company: string;
  date: string;
  timing: 'before_open' | 'after_close';
  epsExpected: number;
  epsPrevious: number;
  revenueExpected: string;
  aiPrediction: 'beat' | 'miss' | 'meet';
  aiConfidence: number;
  expectedMove: number;
}

export interface TradingIdea {
  id: string;
  author: string;
  time: string;
  description: string;
  asset: string;
  direction: 'up' | 'down';
  entry: number;
  target: number;
  stopLoss: number;
  aiScore: number;
  likes: number;
  comments: number;
}

export interface LeaderboardEntry {
  rank: number;
  name: string;
  ideasCount: number;
  successRate: number;
  badge: 'expert' | 'pro' | 'beginner';
}

export interface LessonItem {
  id: string;
  title: string;
  category: string;
  duration: string;
  level: 'مبتدئ' | 'متوسط' | 'متقدم';
  completed: boolean;
  content: string;
  keyPoints: string[];
  practicalExample: string;
}

export interface TermItem {
  term: string;
  full: string;
  description: string;
  category: string;
}

export interface PricingPlan {
  name: string;
  nameAr: string;
  price: number;
  annualPrice: number;
  features: string[];
  highlighted: boolean;
  cta: string;
}

export interface WeeklyReport {
  title: string;
  week: string;
  summary: string;
  insights: string[];
  isPremium: boolean;
}

export interface EbookItem {
  id: string;
  title: string;
  author: string;
  coverUrl: string;
  category: string;
  pages: number;
  price: number;
  currency: string;
  description: string;
  isPremium: boolean;
  rating: number;
  level: string;
  color: string;
  readers: number;
  chapters: { title: string; summary: string }[];
}

export interface AcademyCategory {
  id: string;
  name: string;
  icon: string;
  count: number;
  color: string;
}

export interface CoachSuggestion {
  id: string;
  text: string;
  category: string;
}

export interface CoachFeature {
  id: string;
  title: string;
  description: string;
  icon: string;
}

