// V1040: Locale-aware helpers
// ════════════════════════════════════════════════════════════════════
// Arabic data above is canonical. These helpers return a translated
// view of the data for display in non-Arabic locales. Lesson.content
// and keyPoints remain in Arabic (long-form educational text — too
// much to translate here); only short display fields are translated.

import {
  ACADEMY_CATEGORY_NAMES,
  EBOOK_CATEGORY_NAMES,
  LESSON_DURATION_TRANSLATIONS,
  LESSON_TITLES,
  TERM_FULL_NAMES,
  TERM_DESCRIPTIONS,
  EBOOK_TITLES,
  EBOOK_AUTHORS,
  EBOOK_DESCRIPTIONS,
  type Locale,
} from './academy-translations';

// Map Arabic academy category string → category id used in `academyCategories`
const ACADEMY_CATEGORY_NAME_TO_ID: Record<string, string> = {
  'فوركس': 'forex',
  'تحليل فني': 'technical',
  'تحليل أساسي': 'fundamental',
  'إدارة مخاطر': 'risk',
  'كريبتو': 'crypto',
  'سلع': 'commodities',
  'استراتيجيات': 'strategies',
  'ذكاء اصطناعي': 'ai',
};

/** Returns the localized category name for a given Arabic category string. */
export function translateAcademyCategory(arabicCategory: string, locale: Locale): string {
  if (locale === 'ar') return arabicCategory;
  const id = ACADEMY_CATEGORY_NAME_TO_ID[arabicCategory];
  if (!id) return arabicCategory;
  return ACADEMY_CATEGORY_NAMES[locale]?.[id] ?? arabicCategory;
}

/** Returns the localized ebook category name for a given Arabic category string. */
export function translateEbookCategory(arabicCategory: string, locale: Locale): string {
  if (locale === 'ar') return arabicCategory;
  return EBOOK_CATEGORY_NAMES[locale]?.[arabicCategory] ?? arabicCategory;
}

/** Returns the localized lesson duration for a given Arabic duration string. */
export function translateLessonDuration(arabicDuration: string, locale: Locale): string {
  if (locale === 'ar') return arabicDuration;
  return LESSON_DURATION_TRANSLATIONS[locale]?.[arabicDuration] ?? arabicDuration;
}

/** Returns the localized lesson title for a given lesson id. */
export function translateLessonTitle(lessonId: string, arabicTitle: string, locale: Locale): string {
  if (locale === 'ar') return arabicTitle;
  return LESSON_TITLES[lessonId]?.[locale] ?? arabicTitle;
}

/** Returns the localized financial term full name. */
export function translateTermFull(term: string, arabicFull: string, locale: Locale): string {
  if (locale === 'ar') return arabicFull;
  return TERM_FULL_NAMES[term]?.[locale] ?? arabicFull;
}

/** Returns the localized financial term description. */
export function translateTermDescription(term: string, arabicDescription: string, locale: Locale): string {
  if (locale === 'ar') return arabicDescription;
  return TERM_DESCRIPTIONS[term]?.[locale] ?? arabicDescription;
}

/** Returns the localized ebook title. */
export function translateEbookTitle(ebookId: string, arabicTitle: string, locale: Locale): string {
  if (locale === 'ar') return arabicTitle;
  return EBOOK_TITLES[ebookId]?.[locale] ?? arabicTitle;
}

/** Returns the localized ebook author. */
export function translateEbookAuthor(locale: Locale): string {
  return EBOOK_AUTHORS[locale] ?? EBOOK_AUTHORS.ar;
}

/** Returns the localized ebook description. */
export function translateEbookDescription(ebookId: string, arabicDescription: string, locale: Locale): string {
  if (locale === 'ar') return arabicDescription;
  return EBOOK_DESCRIPTIONS[ebookId]?.[locale] ?? arabicDescription;
}

export type { Locale };

